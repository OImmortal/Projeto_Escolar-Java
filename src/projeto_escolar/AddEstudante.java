    private void emailAlunoFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_emailAlunoFocusGained
        String nomeAluno = this.nomeAluno.getText();
        String[] nomeArray = nomeAluno.split(" ");
        
        String firstName = nomeArray[0];
        String lastNome = nomeArray[nomeArray.length - 1];
        
        
        
        if(nomeArray.length > 1) {
            String email = firstName + "." + lastNome + "@cda.colegiodante.com.br";
            this.emailAluno.setText(email.toLowerCase());
        } else {
            String email = firstName + "@cda.colegiodante.com.br";
            this.emailAluno.setText(email.toLowerCase());
        }

    }//GEN-LAST:event_emailAlunoFocusGained

