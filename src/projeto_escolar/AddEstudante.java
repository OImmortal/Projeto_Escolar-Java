    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nomeAluno = this.nomeAluno.getText();
        String emailAluno = this.emailAluno.getText();
        String telefoneAluno = this.telefoneAluno.getText();
        String nomeResp = this.nomeResp.getText();
        String emailResp = this.emailResp.getText();
        String telefoneResp = this.telefoneResp.getText();
        String endereco = this.endereco.getText();
        String numeroCad = this.numeroCadastro.getText();
        
        try {
            if(numeroCad.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Número de cadastro não informado");
            } else {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/escola", "root", "adminadmin");

                String sql = "insert into tb_alunos values(null,?,?,?,?,?,?,?,?)";

                PreparedStatement ptstmt = conn.prepareStatement(sql);

                ptstmt.setString(1, nomeAluno);
                ptstmt.setString(2, emailAluno);
                ptstmt.setString(3, telefoneAluno);
                ptstmt.setString(4, nomeResp);
                ptstmt.setString(5, emailResp);
                ptstmt.setString(6, telefoneResp);
                ptstmt.setString(7, endereco);
                ptstmt.setString(8, numeroCad);
                JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso");

                ptstmt.execute();
                this.numeroCadastro.setText("");
                conn.close();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

