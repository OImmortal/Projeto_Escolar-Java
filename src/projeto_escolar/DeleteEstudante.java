    private void pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarActionPerformed
        
       
        String sql = "SELECT * FROM tb_alunos ORDER BY nome";
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/escola", "root", "adminadmin");
            
            Statement stmt = conn.createStatement();
            PreparedStatement ptst = conn.prepareStatement(sql);
            ResultSet result = ptst.executeQuery();
            DefaultTableModel tm = (DefaultTableModel) tableAlunos.getModel();
            tm.setRowCount(0);
            while(result.next()) {
                Object o[] = {result.getString("nome"), result.getString("email"), 
                    result.getString("telefone"), result.getString("nome_resp"),
                    result.getString("telefone_resp"),result.getString("numero_cadastro")};
                tm.addRow(o);
            }
            
        } catch (Exception e) {
        }
        
        
    }//GEN-LAST:event_pesquisarActionPerformed

