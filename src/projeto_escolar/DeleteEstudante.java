    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String numeroCad = this.codigo;
        if(numeroCad == null) {
            JOptionPane.showMessageDialog(null, "Selecione um estudante primeiro");
        } else {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/escola", "root", "adminadmin");

                String sql = "DELETE FROM tb_alunos WHERE numero_cadastro = ?";

                PreparedStatement ptst = conn.prepareStatement(sql);
                this.tableAlunos.repaint();
                ptst.setString(1, numeroCad);
                ptst.execute();

                JOptionPane.showMessageDialog(null, "Aluno deletado com sucesso");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

